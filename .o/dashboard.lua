    -- Function to dynamically calculate and apply padding
    local function get_padded_content()
      -- Get the current window height
      local win_height = vim.api.nvim_win_get_height(0)
      -- Number of lines in the ASCII art
      local ascii_art_lines = #ascii_art
      -- Calculate the padding needed to center the content
      local padding_lines = math.max(0, math.floor((win_height - ascii_art_lines) / 2))

      -- Create the padded header
      local padded_ascii_art = {}
      for _ = 1, padding_lines do
        table.insert(padded_ascii_art, '')
      end
      for _, line in ipairs(ascii_art) do
        table.insert(padded_ascii_art, line)
      end

      return padded_ascii_art
    end

